# comparison
a = 3 > 2  # greater than
b = 3 < 2  # less than
c = 3 >= 2  # greater than/equal to
d = 3 <= 2  # less than/equal to
e = 3 == 2  # equal to
f = 3 != 2  # not equal to
print(a, b, c, d, e, f)

# logical operators
price = 25
print(price > 10 and price < 30)
print(price > 10 or price < 30)
print(not price > 10)
