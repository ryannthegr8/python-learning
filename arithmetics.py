print(10 // 3)  # division with no decimal
print(10 / 3)  # normal division
print(10 * 3)  # product
print(10 - 3)  # subtraction
print(10 + 3)  # addition
print(10 % 3)  # modulus
print(10 ** 3)  # exponentiation


